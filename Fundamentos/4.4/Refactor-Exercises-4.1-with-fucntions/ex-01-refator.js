function soma(a,b){
    let soma = a+b;
    return soma;
}

function subtracao(a,b){
    let subtracao = a-b;
    return subtracao;
}

function multiplicacao(a,b){
    let multiplicacao = a * b;
    return multiplicacao;
}

function divisao(a,b){
    let divisao = a/b;
    return divisao;
}

function modulo(a,b){
    let modulo = a % b;
    return modulo;    
}
console.log('A soma é:',soma(4,6));
console.log('A subtração é',subtracao(4,6));
console.log('A multiplicacão é',multiplicacao(4,6));
console.log('A divisão é: ',divisao(4,6));
console.log('O módulo é: ',modulo(4,6));
